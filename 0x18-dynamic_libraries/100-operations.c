#include "100-operations.h"

/**
 * mul - mul
 *
 * @a: int1
 * @b: int2
 * Return: mul
 *
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * add - add
 *
 * @a: int1
 * @b: int2
 *
 * Return: addition
 *
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub
 *
 * @a: int1
 * @b: int2
 *
 * Return: diff
 *
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * div - div
 *
 * @a: int1
 * @b: int2
 *
 * Return: div
 *
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod
 *
 * @a: int1
 * @b: int2
 *
 * Return: mod
 *
 */
int mod(int a, int b)
{
	return (a % b);
}
